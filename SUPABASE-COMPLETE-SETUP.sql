create or replace function public.refresh_seller_balance_from_ledger()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_seller uuid;
begin
  target_seller := case when tg_op = 'DELETE' then old.seller_id else new.seller_id end;
  insert into public.seller_balances (
    user_id, available_balance, pending_balance, paid_balance,
    platform_fee_total, last_order_at, updated_at
  )
  select
    target_seller,
    coalesce(sum(l.seller_amount) filter (where l.status in ('pending_clearance','available','payout_requested')), 0),
    coalesce(sum(l.seller_amount) filter (where l.status in ('pending_clearance','available','payout_requested')), 0),
    coalesce(sum(l.seller_amount) filter (where l.status = 'paid'), 0),
    coalesce(sum(l.platform_fee), 0),
    max(l.created_at),
    now()
  from public.seller_ledger l
  where l.seller_id = target_seller
  on conflict (user_id) do update set
    available_balance = excluded.available_balance,
    pending_balance = excluded.pending_balance,
    paid_balance = excluded.paid_balance,
    platform_fee_total = excluded.platform_fee_total,
    last_order_at = excluded.last_order_at,
    updated_at = now();
  if tg_op = 'DELETE' then
    return old;
  end if;
  return new;
end;
$$;

