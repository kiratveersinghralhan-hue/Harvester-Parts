do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'users','sellers','products','orders','messages','membership_purchases',
    'seller_ledger','seller_balances','seller_payout_requests',
    'site_carousel_slides','seller_reviews','admin_notifications'
  ]
  loop
    if not exists (
      select 1
      from pg_publication_tables
      where pubname = 'supabase_realtime'
        and schemaname = 'public'
        and tablename = table_name
    ) then
      begin
        execute format('alter publication supabase_realtime add table public.%I', table_name);
      exception
        when duplicate_object then null;
        when undefined_object then
          raise notice 'supabase_realtime publication is unavailable; enable Realtime in the dashboard.';
      end;
    end if;
  end loop;
end $$;

