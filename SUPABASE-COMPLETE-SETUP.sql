  column_status as (
    select
      e.table_name,
      count(*) filter (where c.column_name is null) as missing_count,
      string_agg(x.column_name, ', ' order by x.column_name) filter (where c.column_name is null) as missing_columns
    from expected_columns e
    cross join lateral unnest(e.columns) as x(column_name)
    left join information_schema.columns c
      on c.table_schema = 'public'
      and c.table_name = e.table_name
      and c.column_name = x.column_name
    group by e.table_name
  ),
  expected_constraints(name) as (
    values
      ('users_auth_id_fkey'),('sellers_user_id_fkey'),('products_user_id_fkey'),
      ('products_seller_id_fkey'),('orders_buyer_id_fkey'),('order_items_order_id_fkey'),
      ('messages_sender_id_fkey'),('messages_receiver_id_fkey'),('reports_reporter_id_fkey'),
      ('membership_purchases_user_id_fkey'),('seller_payout_accounts_user_id_fkey'),
      ('seller_balances_user_id_fkey'),('seller_payout_requests_user_id_fkey'),
      ('seller_ledger_seller_id_fkey'),('seller_reviews_seller_id_fkey'),
      ('seller_reviews_reviewer_id_fkey')
  ),
  expected_buckets(name) as (
    values ('profile-avatars'),('product-images'),('verification-docs')
  ),
  realtime_tables(name) as (
    values ('users'),('sellers'),('products'),('orders'),('messages'),
      ('membership_purchases'),('seller_ledger'),('seller_balances'),
      ('seller_payout_requests'),('site_carousel_slides'),('seller_reviews'),
      ('admin_notifications')
  )
  select 'table', t.name,
    case when to_regclass('public.' || t.name) is not null then 'OK' else 'MISSING' end
  from expected_tables t
  union all
  select 'columns', c.table_name,
    case when c.missing_count = 0 then 'OK' else 'MISSING: ' || c.missing_columns end
  from column_status c
  union all
  select 'relationship', e.name,
    case when exists (select 1 from pg_constraint pc where pc.conname = e.name) then 'OK' else 'MISSING' end
  from expected_constraints e
  union all
  select 'view', 'public_seller_profiles',
    case when to_regclass('public.public_seller_profiles') is not null then 'OK' else 'MISSING' end
  union all
  select 'bucket', b.name,
    case when exists (select 1 from storage.buckets sb where sb.id = b.name) then 'OK' else 'MISSING' end
  from expected_buckets b
  union all
  select 'realtime', r.name,
    case when exists (
      select 1 from pg_publication_tables p
      where p.pubname = 'supabase_realtime'
        and p.schemaname = 'public'
        and p.tablename = r.name
    ) then 'OK' else 'MISSING' end
  from realtime_tables r;
